func TestSplitOffRootCert(t *testing.T) {

	chain := myintermediate2 + myintermediate1 + myroot
	root, rest, err := SplitOffRootCertPEM(chain)
	assert.NoError(t, err)
	assert.Equal(t, myroot, root)
	assert.Equal(t, myintermediate2+myintermediate1, rest)

	//Corner case: No chain

	chain = ""
	root, rest, err = SplitOffRootCertPEM(chain)
	assert.NoError(t, err)
	assert.Equal(t, root, "")
	assert.Equal(t, rest, "")

	//Corner case: One element in chain

	chain = myroot
	root, rest, err = SplitOffRootCertPEM(chain)
	assert.NoError(t, err)
	assert.Equal(t, root, myroot)
	assert.Equal(t, rest, "")

}
